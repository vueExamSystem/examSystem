<template>
	<section class="panel" id="sameAddForm">
		<div class="title">
			<span>添加题组</span>
			<div class="pull-right">
				<el-button type="success" @click="onSubmit('ruleForm')" class="el-button-shadow">保存</el-button>
				<el-button type="danger" @click="resetForm('ruleForm')" class="el-button-shadow">取消</el-button>
			</div>
		</div>

		<div class="content">
			<el-form :model="ruleForm"
					 v-loading="loading"
					 :inline-message="isInlineMessage"
					 :rules="rules" ref="ruleForm" label-width="100px" class="demo-ruleForm">
				<el-form-item label="题组名称" prop="name">
					<el-input v-model="ruleForm.name"></el-input>
				</el-form-item>
                <el-form-item label="试题类型" prop="questionType">
                    <el-select v-model="ruleForm.questionType" placeholder="请选择试题类型">
                        <el-option
                                v-for="item in questionTypeArr"
                                :label="item.text"
                                :value="item.value"
                                :key="item.value"
                        >
                        </el-option>
                    </el-select>
                </el-form-item>
				<el-form-item label="所属课程" prop="course">
					<el-select
							v-model="ruleForm.course"
							placeholder="请选择所属课程"
							@change="changeCourse"
					>
						<el-option
								v-for="item in courseArr"
								:label="item.text"
								:value="item.value"
								:key="item.value"
						>
						</el-option>
					</el-select>
				</el-form-item>
				<el-form-item label="所属章节" prop="chapter">
					<el-select v-model="ruleForm.chapter" placeholder="请选择所属章节">
						<el-option
								v-for="item in chapterArr"
								:label="item.text"
								:value="item.value"
								:key="item.value"
						>
						</el-option>
					</el-select>
				</el-form-item>
				<el-form-item label="选择试题" prop="ids">
					<el-button type="primary" icon="iconfont icon-plus" @click="addQuestion()">添加试题</el-button>
					<div class="tagArea" v-model="ruleForm.ids" >
						<el-tag
								v-for="tag in ruleForm.ids"
								:key="tag.id"
								closable
								@close="tagClose(tag.id)"
								type="info">
							{{tag.label}}
						</el-tag>
					</div>
				</el-form-item>
				<el-form-item label="题组描述" prop="desc">
					<el-input
							type="textarea"
							:rows="3"
							placeholder="请输入题组描述"
							v-model="ruleForm.desc">
					</el-input>
				</el-form-item>
			</el-form>
		</div>

		<!--编辑界面-->
		<el-dialog title="添加试题" :visible.sync="addFormVisible">
			<el-tree
					ref="tree"
					highlight-current
					:data="addRows"
					:props="defaultProps"
					show-checkbox
					node-key="id"
					v-loading="treeLoading"
					default-expand-all
					:default-checked-keys="addListValue"
					:expand-on-click-node="false">
			</el-tree>
			<div slot="footer" class="dialog-footer">
				<el-button @click.native="addFormVisible = false">取消</el-button>
				<el-button type="primary" @click="getCheckedNodes">提交</el-button>
			</div>
		</el-dialog>
	</section>

</template>

<script>
    import {
        getSameFilter,
		addSame,
        selectQuestions,
    } from '../../../api/api';
    import _ from 'lodash';
	export default {
		data() {
			return {
                ruleForm: {
                    name: '',
                    desc: '',
					chapter: '',
                    course: '',
                    questionType:'',
					ids: [],
                },
                rules: {
                    name: [
                        { required: true, message: '请填写题组名称', trigger: 'blur' }
                    ],
                    questionType: [
                        { required: true, message: '请选择试题类型', trigger: 'change' }
                    ],
                    course: [
                        { required: true, message: '请选择所属课程', trigger: 'change' }
                    ],
                    chapter: [
                        { required: true, message: '请选择所属章节', trigger: 'change' }
                    ],
                },

                questionTypeArr:[],
                courseArr: [],
                chapterArr: [],
                chapterAllArr: [],
				loading: false,
                isInlineMessage: true,

                addFormVisible: false,
                treeLoading: false,
                addRows: [],
                defaultProps: {
                    children: 'children',
                    label: 'label',
                },
				// addList: [],
                addListValue: [],
			}
		},
		methods: {
            getCheckedNodes() {
                const checkArr = this.$refs.tree.getCheckedNodes();
                let para = checkArr.filter(item => { return !item.children });
                // para = para.map(item => item.id);
                // 弹出框选择的题目
                console.log(para);
                this.$confirm('确认添加相似题组吗？', '提示', {}).then(() => {
                    this.ruleForm.ids = para;
                    this.addListValue = this.ruleForm.ids.map(item => item.id);
                    this.addFormVisible = false;
                });
            },
            tagClose(id) {
                const index = _.findIndex(this.ruleForm.ids, { id: id });
                this.ruleForm.ids.splice(index, 1);
                this.addListValue = this.ruleForm.ids.map(item => item.id);
			},
            onSubmit(formName) {
                this.$refs[formName].validate((valid) => {
                    if (valid) {
                        this.$confirm('确认添加吗？', '提示', {}).then(() => {
                            let para = _.assign({}, this.ruleForm);
                            console.log('addDemo',para);
                            //todo 试题id集合
                            var ids=[0];//如[1,2] JSON.stringify(ids);
                            para={
                                name:this.ruleForm.name,
                                questionTypeId:this.ruleForm.questionType,
                                courseId:this.ruleForm.course,
                                sectionId:this.ruleForm.chapter,
                                desc:this.ruleForm.desc,
                                'ids':this.ruleForm.ids.map(item => item.id),
                            };
                            console.log('reqPara',para);
                            this.loading = true;
                            addSame(para).then((res) => {
                                if (res.code !== 0) {
                                    this.$message({
                                        message: res.msg,
                                        type: 'error'
                                    });
                                } else {
                                    this.$message({
                                        message: '提交成功',
                                        type: 'success'
                                    });
                                    this.loading = false;
                                    this.$refs['ruleForm'].resetFields();
                                    this.$emit('toTable');
                                }

                            });
                        });
                    } else {
                        console.log('error submit!!');
                        return false;
                    }
                });
            },
            resetForm(formName) {
                this.$refs[formName].resetFields();
            },
            // 获取初始数据
            getDefaultData() {
               getSameFilter({}).then((res) => {
                    console.log('getSameFilter',res);
                    this.courseArr = res.data[0].children;
                    //todo所有章节数据 value text courseid为课程筛选id
                    this.chapterArr=res.data[1].children;
                    this.chapterAllArr = res.data[1].children;
                    this.questionTypeArr=res.data[2].children;
                });
            },
            changeCourse(val) {
                this.chapterArr = this.chapterAllArr.filter(item => { return item.courseid === val });
                this.ruleForm.chapter = '';
			},
			// 添加试题事件
            addQuestion() {
                //todo希望可以获得下拉框courseName和sectionName
                let para={
                    courseId:this.ruleForm.course,
                    courseName:'',
                    sectionId:this.ruleForm.chapter,
                    sectionName:'',
                    questionTypeId:this.ruleForm.questionType
                } ;
                //若courseId或sectionId或questionTypeId有一为空 可以查询试题
                //console.log('addQuestion',para);
                this.addFormVisible = true;
                this.treeLoading = true;
                selectQuestions(para).then(res => {
                    console.log('selectQuestions',res);
                    this.addRows = res.data ? [res.data] : [];
                    this.treeLoading = false;
                });
                //todo选择完毕后 选择的试题展示到页面上
			},
		},
        computed: {
		},
        mounted() {
		    this.getDefaultData();
        }
	}

</script>

